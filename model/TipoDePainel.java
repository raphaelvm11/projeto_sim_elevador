package model;

// Enumeração que define os tipos de painéis do elevador
public enum TipoDePainel {
    // Painel com um botão para chamar o elevador
    UNICO_BOTAO,

    // Painel com dois botões (subir e descer)
    DOIS_BOTOES,

    // Painel com teclas numéricas para escolher o andar destino
    PAINEL_NUMERICO
}
